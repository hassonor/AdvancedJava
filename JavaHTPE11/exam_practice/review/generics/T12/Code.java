package JavaHTPE11.exam_practice.review.generics.T12;

public class Code {
    private String txt;
    private int index;

    public Code(String txt, int index) {
        this.txt = txt;
        this.index = index;
    }

    public String getText() {
        return txt;
    }

    public int getIndex() {
        return index;
    }
}
